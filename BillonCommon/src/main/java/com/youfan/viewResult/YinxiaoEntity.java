package com.youfan.viewResult;

public class YinxiaoEntity {
    private String numRange;
    private String status;
    private String usernums;
    private String productnums;
    private String shopnums;
    private String merchatnums;

    public String getNumRange() {
        return numRange;
    }

    public void setNumRange(String numRange) {
        this.numRange = numRange;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUsernums() {
        return usernums;
    }

    public void setUsernums(String usernums) {
        this.usernums = usernums;
    }

    public String getProductnums() {
        return productnums;
    }

    public void setProductnums(String productnums) {
        this.productnums = productnums;
    }

    public String getShopnums() {
        return shopnums;
    }

    public void setShopnums(String shopnums) {
        this.shopnums = shopnums;
    }

    public String getMerchatnums() {
        return merchatnums;
    }

    public void setMerchatnums(String merchatnums) {
        this.merchatnums = merchatnums;
    }
}
