package com.youfan.product;

public class Merchart {
   private Integer id;
  private String merchartName;
    private String  merchartArea;
  private Integer  shopnum;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getMerchartName() {
        return merchartName;
    }

    public void setMerchartName(String merchartName) {
        this.merchartName = merchartName;
    }

    public String getMerchartArea() {
        return merchartArea;
    }

    public void setMerchartArea(String merchartArea) {
        this.merchartArea = merchartArea;
    }

    public Integer getShopnum() {
        return shopnum;
    }

    public void setShopnum(Integer shopnum) {
        this.shopnum = shopnum;
    }
}
